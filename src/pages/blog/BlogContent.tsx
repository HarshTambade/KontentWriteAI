import React, { useState } from 'react';
import { PageHeader } from '../../components/ui/PageHeader';
import { ContentForm } from '../../components/ui/ContentForm';
import { generateContent } from '../../lib/gemini';
import { blogPrompts } from '../../lib/prompts/blogPrompts';
import { useApiKeyStore } from '../../lib/store';

export const BlogContent = () => {
  const [outline, setOutline] = useState('');
  const [loading, setLoading] = useState(false);
  const [content, setContent] = useState('');
  const [error, setError] = useState('');
  const apiKey = useApiKeyStore((state) => state.apiKey);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!apiKey) {
      setError('Please set your Gemini API key first');
      return;
    }

    setLoading(true);
    setError('');
    
    try {
      const result = await generateContent(blogPrompts.content(outline));
      setContent(result);
    } catch (err) {
      setError('Failed to generate content. Please try again.');
      console.error(err);
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="space-y-6">
      <PageHeader
        title="Blog Content Generator"
        description="Generate comprehensive blog content from your outline"
      />

      <ContentForm onSubmit={handleSubmit} loading={loading}>
        <div>
          <label htmlFor="outline" className="block text-sm font-medium text-gray-700">
            Blog Outline
          </label>
          <textarea
            id="outline"
            value={outline}
            onChange={(e) => setOutline(e.target.value)}
            className="mt-1 block w-full rounded-md border border-gray-300 px-3 py-2 focus:border-blue-500 focus:outline-none"
            placeholder="Paste your blog outline here"
            rows={8}
            required
          />
        </div>
      </ContentForm>

      {error && (
        <div className="rounded-md bg-red-50 p-4 text-red-700">
          {error}
        </div>
      )}

      {content && (
        <div className="rounded-lg border border-gray-200 bg-white p-6">
          <pre className="whitespace-pre-wrap">{content}</pre>
        </div>
      )}
    </div>
  );
};